CREATE TABLE stg_match_scores (
  tourney_year_id       VARCHAR2(15),
  tourney_round_name    VARCHAR2(20),
  tourney_url           VARCHAR2(200),
  round_order           NUMBER,
  match_order           NUMBER,
  match_id              VARCHAR2(40),
  match_score           VARCHAR2(40),
  match_stats_url       VARCHAR2(200),
  match_ret             VARCHAR2(10),
  winner_url            VARCHAR2(200),
  winner_code           VARCHAR2(200),
  winner_seed           VARCHAR2(5),
  winner_sets_won       NUMBER,
  winner_games_won      NUMBER,
  winner_tiebreaks_won  NUMBER,
  loser_url             VARCHAR2(200),
  loser_code            VARCHAR2(200),
  loser_seed            VARCHAR2(5),
  loser_sets_won        NUMBER,
  loser_games_won       NUMBER,
  loser_tiebreaks_won   NUMBER,
  batch_id              NUMBER(12)
);
